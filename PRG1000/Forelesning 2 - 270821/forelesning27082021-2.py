#Mattematiske operatorer

#Multiplikasjon,*
produkt=8*7
print ('produktet er', produkt)

#Divisjon, /
resultat=76/4
print('Resultatet av 76:4 er', resultat)

#Heltalldivisjon, //
resultat=77/4
heltallsdivisjon=77//4
print ('Resultatet av 77:4',resultat)
print ('Heltallsdivisjonen av 77:4',heltallsdivisjon)

#Restdivisjon, %
resultat=77/4
restdivisjon=77%4
print('Resultatet av 77:4 er',resultat)
print('Restdivisjonen av 77:4 er', restdivisjon)
