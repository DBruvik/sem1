sugar=150
butter=100
flour=275
cookiesMade=int(input('How many cookies do you want to make?' ))
sugar1=sugar/48
butter1=butter/48
flour1=flour/48
print (f' You need {cookiesMade*sugar1/100:.2f} cup of sugar.')
print (f' You need {cookiesMade*butter1/100:.2f} cup of butter.')
print (f' You need {cookiesMade*flour1/100:.2f} cup of flour \
for {cookiesMade} cookies! :)')


      
    
